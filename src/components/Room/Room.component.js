import MainScreen from "../MainScreen/MainScreen.component";
import { useEffect } from "react";
import { useLocation, useParams } from 'react-router-dom';
import "./Room.css";
import {
  setMainStream,
  addParticipant,
  setUser,
  removeParticipant,
  updateParticipant,
} from "../../store/actioncreator";
import { connect } from "react-redux";
import { db } from '../../server/firebase.js';


var firepadRef = db.database().ref();

function Room(props) {

  const {roomId} = useParams();
  const connectedRef = db.database().ref(".info/connected");
  const participantRef = firepadRef.child(roomId);  //firepadRef
  // const participantRef = firepadRef.child("participants");
  const location = useLocation();
  const userName = new URLSearchParams(location.search).get('name');
  const isUserSet = !!props.user;
  const isStreamSet = !!props.stream;

  const getUserStream = async () => {
    const localStream = await navigator.mediaDevices.getUserMedia({
      audio: true,
      video: true,
    });

    return localStream;
  };

  const handleConnection = async () => {
    const stream = await getUserStream();
    stream.getVideoTracks()[0].enabled = false;
    props.setMainStream(stream);

    connectedRef.on("value", (snap) => {
      if (snap.val()) { //checking if this user is connected to firebase database
        const defaultPreference = {
          audio: true,
          video: false,
          screen: false,
        };
        const userStatusRef = participantRef.push({
          userName,
          preferences: defaultPreference,
        });
        props.setUser({ //It sets the user's data in the store, using the key generated by push() as the key for the user object. The user object includes the userName and defaultPreference.
          [userStatusRef.key]: { name: userName, roomId: roomId, ...defaultPreference },
        });
        userStatusRef.onDisconnect().remove();
      }
    });
  };

  useEffect(() => {
    handleConnection();
  }, []);
  

  //This useEffect hook sets up listeners to handle child added and child removed events on the participantRef Firebase Realtime Database reference.
  useEffect(() => {
    if (isStreamSet && isUserSet) {
      participantRef.on("child_added", (snap) => {
        const preferenceUpdateEvent = participantRef
          .child(snap.key)
          .child("preferences");
        preferenceUpdateEvent.on("child_changed", (preferenceSnap) => {
          props.updateParticipant({
            [snap.key]: {
              [preferenceSnap.key]: preferenceSnap.val(),
            },
          });
        });
        const { userName: name, preferences = {} } = snap.val();
        props.addParticipant({
          [snap.key]: {
            name,
            ...preferences,
          },
        });
      });
      participantRef.on("child_removed", (snap) => {
        props.removeParticipant(snap.key);
      });
    }
  }, [isStreamSet, isUserSet]);

  return (
    <div className="Room">
      <MainScreen />
    </div>
  );
}

const mapStateToProps = (state) => {
  return {
    stream: state.mainStream,
    user: state.currentUser,
  };
};

const mapDispatchToProps = (dispatch) => {
  return {
    setMainStream: (stream) => dispatch(setMainStream(stream)),
    addParticipant: (user) => dispatch(addParticipant(user)),
    setUser: (user) => dispatch(setUser(user)),
    removeParticipant: (userId) => dispatch(removeParticipant(userId)),
    updateParticipant: (user) => dispatch(updateParticipant(user)),
  };
};

export default connect(mapStateToProps, mapDispatchToProps)(Room);


